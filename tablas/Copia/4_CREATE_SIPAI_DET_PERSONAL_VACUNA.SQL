CREATE SEQUENCE SIPAI_S_PERSONA_VACUNA_ID
  START WITH 1
  MAXVALUE 999999999
  MINVALUE 1
  NOCYCLE
  NOCACHE
  ORDER
  NOKEEP
  GLOBAL;
  
  
CREATE TABLE SIPAI_DET_PERSONAL_VACUNA
(
  PERSONAL_VACUNA_ID            NUMBER(10) CONSTRAINT PK_SIPPER_PERSONA_ID PRIMARY KEY NOT NULL,
  PRIMER_NOMBRE                 VARCHAR2(50) CONSTRAINT NNC_SIPPER_PNOMBRE NOT NULL,
  SEGUNDO_NOMBRE                VARCHAR2(50), 
  PRIMER_APELLIDO               VARCHAR2(50) CONSTRAINT NNC_SIPPER_PAPELLIDO NOT NULL,
  SEGUNDO_APELLIDO              VARCHAR2(50),
  CODIGO                        VARCHAR2(30) CONSTRAINT NNC_SIPPER_CODIGO NOT NULL,
  ESTADO_REGISTRO_ID            NUMBER(10) CONSTRAINT NNC_SIPPER_ESTADO_REG_ID NOT NULL,
  USUARIO_REGISTRO              VARCHAR2(50 BYTE) CONSTRAINT NNC_SIPPER_USR_REGISTRO NOT NULL,
  FECHA_REGISTRO                TIMESTAMP(0)   DEFAULT SYSTIMESTAMP CONSTRAINT NNC_SIPPER_FECHA_REGISTRO NOT NULL,
  USUARIO_MODIFICACION          VARCHAR2(50 BYTE),
  FECHA_MODIFICACION            TIMESTAMP(0),
  USUARIO_PASIVA                VARCHAR2(50 BYTE),
  FECHA_PASIVO                  TIMESTAMP(0)
  );
  

CREATE UNIQUE INDEX IDX_UI_SIPPER_PERSONAL_ID ON SIPAI_DET_PERSONAL_VACUNA
(PERSONAL_VACUNA_ID);

CREATE INDEX IDX_SIPREL_S_NMB_APLL ON SIPAI_DET_PERSONAL_VACUNA
(SEGUNDO_APELLIDO, SEGUNDO_NOMBRE);

CREATE INDEX IDX_SIPREL_PRIMER_NOMBRE ON SIPAI_DET_PERSONAL_VACUNA
(PRIMER_NOMBRE);


CREATE INDEX IDX_SIPREL_PRIMER_APELLIDO ON SIPAI_DET_PERSONAL_VACUNA
(PRIMER_APELLIDO);

CREATE INDEX IDX_SIPREL_SEGUNDO_NOMBRE ON SIPAI_DET_PERSONAL_VACUNA
(SEGUNDO_NOMBRE);

CREATE INDEX IDX_SIPREL_SEGUNDO_APELLIDO ON SIPAI_DET_PERSONAL_VACUNA
(SEGUNDO_APELLIDO);


CREATE INDEX IDX_SIPPER_CODIGO ON SIPAI_DET_PERSONAL_VACUNA
(CODIGO);

CREATE INDEX IDX_SIPPER_ESTADO_ID ON SIPAI_DET_PERSONAL_VACUNA
(ESTADO_REGISTRO_ID);


  
CREATE OR REPLACE TRIGGER TRG_AUD_SIPPER_PERSONA_VACUNA
BEFORE INSERT OR UPDATE ON SIPAI_DET_PERSONAL_VACUNA FOR EACH ROW
BEGIN
    IF INSERTING THEN
       :NEW.PERSONAL_VACUNA_ID  := SIPAI_S_PERSONA_VACUNA_ID.NEXTVAL;
       :NEW.FECHA_REGISTRO     := SYSDATE;
    ELSE
       IF :NEW.USUARIO_MODIFICACION IS NULL THEN
           RAISE_APPLICATION_ERROR (-20000, 'El usuario modificación no puede quedar nulo.');
       ELSE
       :NEW.FECHA_MODIFICACION   := SYSDATE;
       END IF;
    END IF;
END;
